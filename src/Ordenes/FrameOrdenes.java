	private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnEliminarActionPerformed

		int rta = JOptionPane.showConfirmDialog(rootPane, "Esta seguro que desea eliminar la orden?");
		if (rta != 0) {
			return;
		} else {
			Integer orden = Integer.parseInt(txtOrden.getText());
			String tipo = txtTipo.getText();

			conn.eliminarOrden(orden, tipo, turno);

			txtOrden.setText("");
			txtTipo.setText("");
			txtMPago.setText("");
			txtTotales.setText("");

			v.visualizar_ordenes(tablap, "", 0);
			tablap.setEnabled(true);

			btnEliminar.setEnabled(false);
			tablap.repaint();

		}
		conn.settotal(txtlocal, txtdelivery, txtefectivo, txtencalle, txtotros);
		vS.visualizar_stock(Stock);
	}// GEN-LAST:event_btnEliminarActionPerformed

