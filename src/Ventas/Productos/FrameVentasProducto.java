    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        if(dtDesde.getDate() == null){
            JOptionPane.showMessageDialog(null, "Debe elegir una fecha inicial valida", "Error de fecha", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(dtHasta.getDate() == null){
            JOptionPane.showMessageDialog(null, "Debe elegir una fecha final valida", "Error de fecha", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        TablasVtaProductos v = new TablasVtaProductos();
        String name= "";
        String ven= "";
        String car = "";
        String turn = "";
      
        if (!txtProducto.getText().equals("")) {
            car = txtProducto.getText();

            Connection con = conn.conectar();
            ResultSet rst = null;
            Boolean existe = false;
            try {
                PreparedStatement ps = con.prepareStatement("SELECT * FROM ventasproductos WHERE nombre = '" + car + "'");
                rst = ps.executeQuery();
                while (rst.next()) {
                    existe = true;
                }
            } catch (Exception ex) {
                System.out.println("No hay registros");
            }
            if (!existe) {
                JOptionPane.showMessageDialog(null, "El producto indicado no existe", "Error en base de datos", JOptionPane.WARNING_MESSAGE);
                return;
            }

            name = "nombre = '" + car + "' AND ";
        }
        
        
        
        if(cmbVenta.getSelectedItem() != "Todas"){
            String aar = (String) cmbVenta.getSelectedItem();
            if(aar == "Local"){
                ven = "tipo = 'local' AND ";
            }else{
                ven = "(tipo = 'delivery' OR tipo = 'deliveryl') AND ";
            }
        }
            
        if(cmbTurno.getSelectedItem() != "Todos"){
            String aar = (String) cmbTurno.getSelectedItem();
            turn = "turno = '"+aar+"' AND ";
        }
        String esdecombo = "";
        if (!checkprodus.isSelected()) {
            esdecombo = " AND total > 0";
        }
        
        
        String formato =  "yyyyMMdd";
        Date dsd = dtDesde.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat(formato);
        String desde = String.valueOf(sdf.format(dsd));
        
        Date hst = dtHasta.getDate();
        SimpleDateFormat sdf2 = new SimpleDateFormat(formato);
        String hasta = String.valueOf(sdf2.format(hst));
        
        v.visualizarVtaProductos(tablaVentas, desde, hasta, name, ven, txtTotal, txtCantidad, turn, esdecombo);
        tablaVentas.setEnabled(false);
    }//GEN-LAST:event_btnBuscarActionPerformed

