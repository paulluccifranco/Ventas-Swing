    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        if(dtDesde.getDate() == null){
            JOptionPane.showMessageDialog(null, "Debe elegir una fecha valida", "Error de fecha", JOptionPane.WARNING_MESSAGE);
            return;
        }

        txtArea.setText("Resumen no disponible");
        String turno = (String) cmbTurno.getSelectedItem();

        TablasVtaProductos v = new TablasVtaProductos();
        String formato = "yyyyMMdd";
        Date dsd = dtDesde.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat(formato);
        String desde = String.valueOf(sdf.format(dsd));
        
        ResultSet rs = conn.buscarticket(desde, turno);
        
        try {
            while (rs.next()) {
                txtArea.setText(rs.getString(1));
            }
        } catch (Exception ex) {
            System.out.println(ex);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed


